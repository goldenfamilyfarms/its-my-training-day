// Reconcile is the main reconciliation loop for the GrafanaDashboard controller.
//
// The reconciliation pattern:
// 1. Fetch the custom resource
// 2. Check if it's being deleted (handle finalizers)
// 3. Validate the spec
// 4. Perform the actual work (sync to Grafana)
// 5. Update the status
// 6. Return result (requeue if needed)
//
// Key principles:
// - Idempotent: Running reconcile multiple times should produce the same result
// - Level-triggered: React to the current state, not events
// - Eventually consistent: Keep trying until desired state is achieved
func (r *GrafanaDashboardReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	log := r.Log.WithValues("grafanadashboard", req.NamespacedName)
	log.Info("Starting reconciliation")

	// -------------------------------------------------------------------------
	// Step 1: Fetch the GrafanaDashboard resource
	// -------------------------------------------------------------------------
	dashboard := &observabilityv1alpha1.GrafanaDashboard{}
	if err := r.Get(ctx, req.NamespacedName, dashboard); err != nil {
		if errors.IsNotFound(err) {
			// Resource was deleted before reconciliation
			// This is normal - just log and return without error
			log.Info("GrafanaDashboard resource not found, likely deleted")
			return ctrl.Result{}, nil
		}
		// Error reading the resource - requeue with backoff
		log.Error(err, "Failed to fetch GrafanaDashboard")
		return ctrl.Result{RequeueAfter: ErrorRequeueAfter}, err
	}

	// -------------------------------------------------------------------------
	// Step 2: Handle deletion with finalizers
	// -------------------------------------------------------------------------
	// Finalizers ensure we clean up external resources (Grafana dashboards)
	// before the Kubernetes resource is fully deleted

	if dashboard.ObjectMeta.DeletionTimestamp.IsZero() {
		// Resource is not being deleted
		// Ensure our finalizer is present
		if !controllerutil.ContainsFinalizer(dashboard, FinalizerName) {
			log.Info("Adding finalizer")
			controllerutil.AddFinalizer(dashboard, FinalizerName)
			if err := r.Update(ctx, dashboard); err != nil {
				log.Error(err, "Failed to add finalizer")
				return ctrl.Result{}, err
			}
			// Requeue to continue reconciliation with the updated resource
			return ctrl.Result{Requeue: true}, nil
		}
	} else {
		// Resource is being deleted
		if controllerutil.ContainsFinalizer(dashboard, FinalizerName) {
			log.Info("Handling deletion, cleaning up external resources")

			// Clean up the dashboard in Grafana
			if err := r.cleanupDashboard(ctx, dashboard); err != nil {
				log.Error(err, "Failed to cleanup dashboard in Grafana")
				// Record event for visibility
				r.Recorder.Event(dashboard, corev1.EventTypeWarning, "CleanupFailed",
					fmt.Sprintf("Failed to delete dashboard from Grafana: %v", err))
				return ctrl.Result{RequeueAfter: ErrorRequeueAfter}, err
			}

			// Remove the finalizer to allow deletion to proceed
			log.Info("Removing finalizer")
			controllerutil.RemoveFinalizer(dashboard, FinalizerName)
			if err := r.Update(ctx, dashboard); err != nil {
				log.Error(err, "Failed to remove finalizer")
				return ctrl.Result{}, err
			}

			r.Recorder.Event(dashboard, corev1.EventTypeNormal, "Deleted",
				"Dashboard successfully deleted from Grafana")
		}
		// Stop reconciliation as the resource is being deleted
		return ctrl.Result{}, nil
	}

	// -------------------------------------------------------------------------
	// Step 3: Validate the spec
	// -------------------------------------------------------------------------
	if err := r.validateSpec(dashboard); err != nil {
		log.Error(err, "Invalid GrafanaDashboard spec")
		r.Recorder.Event(dashboard, corev1.EventTypeWarning, "ValidationFailed", err.Error())

		// Update status to reflect the error
		if statusErr := r.updateStatus(ctx, dashboard, ConditionTypeSynced, metav1.ConditionFalse,
			ReasonInvalidSpec, err.Error()); statusErr != nil {
			log.Error(statusErr, "Failed to update status")
		}

		// Don't requeue - spec needs to be fixed by user
		return ctrl.Result{}, nil
	}

	// -------------------------------------------------------------------------
	// Step 4: Resolve dashboard JSON content
	// -------------------------------------------------------------------------
	// Dashboard JSON can come from multiple sources:
	// - Inline in spec.json
	// - From a ConfigMap reference
	// - From a URL
	// - From grafana.com

	dashboardJSON, err := r.resolveDashboardJSON(ctx, dashboard)
	if err != nil {
		log.Error(err, "Failed to resolve dashboard JSON")
		r.Recorder.Event(dashboard, corev1.EventTypeWarning, "ResolveFailed",
			fmt.Sprintf("Failed to resolve dashboard JSON: %v", err))

		if statusErr := r.updateStatus(ctx, dashboard, ConditionTypeSynced, metav1.ConditionFalse,
			ReasonSyncFailed, err.Error()); statusErr != nil {
			log.Error(statusErr, "Failed to update status")
		}

		return ctrl.Result{RequeueAfter: ErrorRequeueAfter}, err
	}

	// -------------------------------------------------------------------------
	// Step 5: Sync dashboard to Grafana
	// -------------------------------------------------------------------------
	log.Info("Syncing dashboard to Grafana", "title", dashboard.Spec.Title)

	grafanaDashboard := &Dashboard{
		UID:    dashboard.Status.UID, // Use existing UID if available
		Title:  dashboard.Spec.Title,
		Folder: dashboard.Spec.Folder,
		JSON:   dashboardJSON,
	}

	response, err := r.GrafanaClient.CreateOrUpdateDashboard(ctx, grafanaDashboard)
	if err != nil {
		log.Error(err, "Failed to sync dashboard to Grafana")
		r.Recorder.Event(dashboard, corev1.EventTypeWarning, "SyncFailed",
			fmt.Sprintf("Failed to sync dashboard: %v", err))

		if statusErr := r.updateStatus(ctx, dashboard, ConditionTypeSynced, metav1.ConditionFalse,
			ReasonSyncFailed, err.Error()); statusErr != nil {
			log.Error(statusErr, "Failed to update status")
		}

		return ctrl.Result{RequeueAfter: ErrorRequeueAfter}, err
	}

	// -------------------------------------------------------------------------
	// Step 6: Update status with success
	// -------------------------------------------------------------------------
	log.Info("Dashboard synced successfully", "uid", response.UID, "url", response.URL)

	// Update status fields
	dashboard.Status.UID = response.UID
	dashboard.Status.URL = response.URL
	dashboard.Status.LastSyncTime = &metav1.Time{Time: time.Now()}
	dashboard.Status.ObservedGeneration = dashboard.Generation

	// Calculate content hash for change detection
	dashboard.Status.ContentHash = calculateHash(dashboardJSON)

	// Update the synced condition
	if err := r.updateStatus(ctx, dashboard, ConditionTypeSynced, metav1.ConditionTrue,
		ReasonSyncSuccess, "Dashboard successfully synced to Grafana"); err != nil {
		log.Error(err, "Failed to update status")
		return ctrl.Result{}, err
	}

	// Record success event
	r.Recorder.Event(dashboard, corev1.EventTypeNormal, "Synced",
		fmt.Sprintf("Dashboard synced to Grafana: %s", response.URL))

	// -------------------------------------------------------------------------
	// Step 7: Schedule next reconciliation
	// -------------------------------------------------------------------------
	// Requeue after the configured resync period to ensure the dashboard
	// stays in sync (handles manual changes in Grafana UI)

	requeueAfter := DefaultRequeueAfter
	if dashboard.Spec.ResyncPeriod != "" {
		if parsed, err := time.ParseDuration(dashboard.Spec.ResyncPeriod); err == nil {
			requeueAfter = parsed
		}
	}

	log.Info("Reconciliation complete, scheduling next sync", "requeueAfter", requeueAfter)
	return ctrl.Result{RequeueAfter: requeueAfter}, nil
}

