// validateSpec validates the GrafanaDashboard spec
func (r *GrafanaDashboardReconciler) validateSpec(dashboard *observabilityv1alpha1.GrafanaDashboard) error {
	// Ensure at least one JSON source is specified
	hasJSON := dashboard.Spec.JSON != ""
	hasConfigMapRef := dashboard.Spec.JSONDataFrom != nil && dashboard.Spec.JSONDataFrom.ConfigMapKeyRef != nil
	hasURL := dashboard.Spec.URL != ""
	hasGrafanaCom := dashboard.Spec.GrafanaCom != nil

	sourceCount := 0
	if hasJSON {
		sourceCount++
	}
	if hasConfigMapRef {
		sourceCount++
	}
	if hasURL {
		sourceCount++
	}
	if hasGrafanaCom {
		sourceCount++
	}

	if sourceCount == 0 {
		return fmt.Errorf("must specify one of: json, jsonDataFrom, url, or grafanaCom")
	}
	if sourceCount > 1 {
		return fmt.Errorf("only one JSON source can be specified")
	}

	// Validate instance selector
	if dashboard.Spec.InstanceSelector == nil {
		return fmt.Errorf("instanceSelector is required")
	}

	return nil
}

// resolveDashboardJSON resolves the dashboard JSON from the configured source
func (r *GrafanaDashboardReconciler) resolveDashboardJSON(ctx context.Context, dashboard *observabilityv1alpha1.GrafanaDashboard) (string, error) {
	// Priority: inline JSON > ConfigMap > URL > grafana.com

	// 1. Inline JSON
	if dashboard.Spec.JSON != "" {
		return dashboard.Spec.JSON, nil
	}

	// 2. ConfigMap reference
	if dashboard.Spec.JSONDataFrom != nil && dashboard.Spec.JSONDataFrom.ConfigMapKeyRef != nil {
		ref := dashboard.Spec.JSONDataFrom.ConfigMapKeyRef
		namespace := dashboard.Namespace
		if ref.Namespace != "" {
			namespace = ref.Namespace
		}

		configMap := &corev1.ConfigMap{}
		if err := r.Get(ctx, types.NamespacedName{
			Name:      ref.Name,
			Namespace: namespace,
		}, configMap); err != nil {
			return "", fmt.Errorf("failed to get ConfigMap %s/%s: %w", namespace, ref.Name, err)
		}

		json, ok := configMap.Data[ref.Key]
		if !ok {
			return "", fmt.Errorf("key %s not found in ConfigMap %s/%s", ref.Key, namespace, ref.Name)
		}

		return json, nil
	}

	// 3. URL (would need HTTP client implementation)
	if dashboard.Spec.URL != "" {
		// In a real implementation, fetch from URL
		return "", fmt.Errorf("URL source not implemented in this skeleton")
	}

	// 4. grafana.com (would need grafana.com API client)
	if dashboard.Spec.GrafanaCom != nil {
		// In a real implementation, fetch from grafana.com
		return "", fmt.Errorf("grafana.com source not implemented in this skeleton")
	}

	return "", fmt.Errorf("no JSON source configured")
}

// cleanupDashboard removes the dashboard from Grafana
func (r *GrafanaDashboardReconciler) cleanupDashboard(ctx context.Context, dashboard *observabilityv1alpha1.GrafanaDashboard) error {
	if dashboard.Status.UID == "" {
		// No UID means dashboard was never created in Grafana
		return nil
	}

	return r.GrafanaClient.DeleteDashboard(ctx, dashboard.Status.UID)
}

// updateStatus updates the status conditions of the GrafanaDashboard
func (r *GrafanaDashboardReconciler) updateStatus(
	ctx context.Context,
	dashboard *observabilityv1alpha1.GrafanaDashboard,
	conditionType string,
	status metav1.ConditionStatus,
	reason, message string,
) error {
	// Find existing condition or create new one
	condition := metav1.Condition{
		Type:               conditionType,
		Status:             status,
		ObservedGeneration: dashboard.Generation,
		LastTransitionTime: metav1.Now(),
		Reason:             reason,
		Message:            message,
	}

	// Update or append the condition
	found := false
	for i, c := range dashboard.Status.Conditions {
		if c.Type == conditionType {
			// Only update LastTransitionTime if status changed
			if c.Status != status {
				dashboard.Status.Conditions[i] = condition
			} else {
				// Keep existing transition time, update other fields
				dashboard.Status.Conditions[i].Reason = reason
				dashboard.Status.Conditions[i].Message = message
				dashboard.Status.Conditions[i].ObservedGeneration = dashboard.Generation
			}
			found = true
			break
		}
	}

	if !found {
		dashboard.Status.Conditions = append(dashboard.Status.Conditions, condition)
	}

	// Use status subresource to update only status
	return r.Status().Update(ctx, dashboard)
}

// calculateHash generates a hash of the dashboard content for change detection
func calculateHash(content string) string {
	// In a real implementation, use crypto/sha256
	// This is a placeholder
	return fmt.Sprintf("hash-%d", len(content))
}

