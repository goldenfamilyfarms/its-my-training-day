// SetupWithManager sets up the controller with the Manager.
// This configures:
// - What resources to watch
// - Event filters (predicates)
// - Concurrent reconciliation settings
func (r *GrafanaDashboardReconciler) SetupWithManager(mgr ctrl.Manager) error {
	return ctrl.NewControllerManagedBy(mgr).
		// Primary resource to watch
		For(&observabilityv1alpha1.GrafanaDashboard{}).
		// Watch ConfigMaps that are referenced by dashboards
		// This triggers reconciliation when a referenced ConfigMap changes
		Owns(&corev1.ConfigMap{}).
		// Filter events to reduce unnecessary reconciliations
		WithEventFilter(predicate.Or(
			// Always process create/delete events
			predicate.GenerationChangedPredicate{},
			// Process annotation changes (for manual triggers)
			predicate.AnnotationChangedPredicate{},
		)).
		// Complete the setup
		Complete(r)
}

