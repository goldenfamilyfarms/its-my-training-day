bool detectCycle(ListNode<int>* head) {
    ListNode<int>* slow = head;
    ListNode<int>* fast = head;
    while (fast && fast->next) {
        slow = slow->next;
        fast = fast->next->next;
        if (slow == fast) return true;
    }
    return false;
}
